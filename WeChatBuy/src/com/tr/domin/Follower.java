package com.tr.domin;

public class Follower {
    private String aid;
    private String remark;
    private String urstatus;
    private String addressid;
    private String orderid;
    private String uiconsrc;
    private String account;

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getUiconsrc() {
        return uiconsrc;
    }

    public void setUiconsrc(String uiconsrc) {
        this.uiconsrc = uiconsrc;
    }

    public String getAid() {
        return aid;
    }

    public void setAid(String aid) {
        this.aid = aid;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getUrstatus() {
        return urstatus;
    }

    public void setUrstatus(String urstatus) {
        this.urstatus = urstatus;
    }

    public String getAddressid() {
        return addressid;
    }

    public void setAddressid(String addressid) {
        this.addressid = addressid;
    }

    public String getOrderid() {
        return orderid;
    }

    public void setOrderid(String orderid) {
        this.orderid = orderid;
    }
}
