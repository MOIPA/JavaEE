package com.tr.domin;

public class PostOrderInfo {
    private String theme;
    private String desc;
    private String endtime;
    private String savedPicSrc;
    private String promulgatorid;
    private String com;
    private String postTime;

    public String getPostTime() {
        return postTime;
    }

    public void setPostTime(String postTime) {
        this.postTime = postTime;
    }

    public String getPromulgatorid() {
        return promulgatorid;
    }

    public void setPromulgatorid(String promulgatorid) {
        this.promulgatorid = promulgatorid;
    }

    public String getCom() {
        return com;
    }

    public void setCom(String com) {
        this.com = com;
    }

    public String getTheme() {
        return theme;
    }

    public void setTheme(String theme) {
        this.theme = theme;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getEndtime() {
        return endtime;
    }

    public void setEndtime(String endtime) {
        this.endtime = endtime;
    }

    public String getSavedPicSrc() {
        return savedPicSrc;
    }

    public void setSavedPicSrc(String savedPicSrc) {
        this.savedPicSrc = savedPicSrc;
    }
}
